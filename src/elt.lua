local tostring = tostring

local elt = {}

elt._COPYRIGHT   = 'Copyright (c) 2024-2026 Alejandro Exojo Piqueras'
elt._DESCRIPTION = 'Template engine in pure Lua'
elt._VERSION = '0.1.0'

elt.delimiters = {
    open = '<%',
    close = '%>',
    line = '%',
    raw = '=',
    escape = '!',
}

elt.escape = function(value)
    return value
end

-- Cache the replacement table to avoid reconstructing it on every call.
local escape_html_replacements = {
    ['&']  = '&amp;',  ['<'] = '&lt;',  ['>'] = '&gt;',
    ['"']  = '&quot;', ["'"] = '&#39;', ['`'] = '&#96;',
}

elt.escape_html = function(value)
    return (value:gsub('[&<>"\'`]', escape_html_replacements))
end




--- @class Chunks
-- Simple helper that provides an `append` function to hold the array of chunks
-- of literal strings and code to be generated by the `Generator`.
--- @alias Chunks.Kind `Chunks.CODE`|`Chunks.RAW`|`Chunks.ESCAPE`
elt.Chunks = {
    CODE = 1,
    RAW = 2,
    ESCAPE = 3,

    --- Creates a new instance.
    --- @param self Chunks
    --- @return Chunks
    new = function(self)
        return setmetatable({}, self)
    end,

    --- Appends a string to the list of chunks.
    ---
    --- The string can be one of four kinds.
    ---  * A literal string to be put in the output verbatim.
    ---  * A fragment of Lua code to be inserted as is (`Chunks.CODE`).
    ---  * A value to be printed verbatim after converted to string (`Chunks.RAW`).
    ---  * A value to be printed after escaping (`Chunks.ESCAPE`).
    --- The last 3 require a numeric position and a kind as arguments.
    --- @overload fun(self: Chunks, value: string)
    --- @overload fun(self: Chunks, value: string, position: integer, kind: Chunks.Kind)
    append = function(self, value, position, kind)
        assert(self and value and type(value) == 'string')
        assert((kind and position) or (not kind and not position))
        if not position then
            table.insert(self, value)
        else
            table.insert(self, {value, position, kind})
        end
    end,

}
elt.Chunks.__index = elt.Chunks




--- @class Generator
-- The class that generates the string of Lua code that will be compiled into
-- the function that renders the template.
elt.Generator = {
    new = function(self)
        local instance = setmetatable({}, self)
        instance.buffer = {}
        return instance
    end,

    result = function(self)
        return table.concat(self.buffer)
    end,

    push = function(self, text, ...)
        table.insert(self.buffer, text)
        if ... then
            self:push(...)
        end
    end,

    header = function(self)
        self:push('local __insert, __buffer, __stringify, __escape = ...\n')
    end,

    footer = function(self)
        self:push('return __buffer\n')
    end,

    mark = function(self, pos)
        self:push('--[[', tostring(pos), ']] ')
    end,

    assign = function(self, value)
        self:push('__insert(__buffer, ', value, ')\n')
    end,

    generate = function(self, chunks)
        self.buffer = {}
        self:header()
        for _, chunk in ipairs(chunks) do
            local kind = type(chunk)
            -- If it's a string, it should be put as a string literal in the
            -- generated code, just escaping the quote symbols using format().
            if kind == 'string' then
                self:assign(('%q'):format(chunk))
            elseif kind == 'table' then
                local value = chunk[1]
                local position = chunk[2]
                kind = chunk[3]

                self:mark(position)
                -- If it's code, it's just added to the buffer, raw.
                if kind == elt.Chunks.CODE then
                    self:push(value, '\n')
                -- Similar to the literal string, but converting to string (and
                -- perhaps escaping).
                elseif kind == elt.Chunks.RAW then
                    self:assign('__stringify(' .. value .. ')')
                elseif kind == elt.Chunks.ESCAPE then
                    self:assign('__escape(__stringify(' .. value .. '))')
                end
            else
                error('unknown type ' .. tostring(kind))
            end
        end
        self:footer()
        return self:result()
    end
}
elt.Generator.__index = elt.Generator




--- @class Parser
elt.Parser = {
    new = function(self)
        local delimiters = {}
        for key, value in pairs(elt.delimiters) do
            delimiters[key] = self.delimiters and self.delimiters[key] or value
        end
        return setmetatable({delimiters = delimiters}, elt.Parser)
    end,

    wrap_source = function(source)
        if type(source) == 'string' then
            local position = 1
            return function()
                if position > #source then
                    return nil
                end
                local start, finish = source:find('\r?\n', position)
                if start then
                    local line = source:sub(position, start - 1)
                    position = finish + 1
                    return line
                else -- Return the last line, even if not ended in (CR)LF.
                    local line = source:sub(position)
                    position = #source + 1
                    return line
                end
            end
        elseif type(source) == 'table' then
            -- To prevent silly mistakes, like doing self:wrap_source, and
            -- accidentally passing the Parser instance as source.
            assert(getmetatable(source) ~= elt.Parser, '`wrap_source` does not need a `self`')
            -- The table can be empty, but if not empty, it must have strings.
            assert(#source == 0 or type(source[1]) == 'string',
                'Source of data for parsing should be a list of strings')
            local current = 0
            return function()
                current = current + 1
                return current <= #source and source[current] or nil
            end
        elseif type(source) == 'function' then
            return source
        else
            error('Parser: unsupported source type. Use a string, table or iterator')
        end
    end,

    parse = function(self, source, delimiters)
        delimiters = delimiters or self.delimiters
        local chunks = elt.Chunks:new()
        local plain_text = true -- Constant. For clarity in string.find() calls.
        --- @type (boolean|Chunks.Kind) Tracks if we are in plain text (`false`)
        --- or in which kind of "special" code context (code, raw or escaped).
        local special = false -- Start with "text", till we find a delimiter.

        -- Keep track of the line number in the template, so the generated code
        -- can be mapped back to the template lines in errors.
        local line_count = 0

        -- Tracks text or interpolated values (RAW, ESCAPE) that are not added
        -- line by line, but accumulated and appended in one go.
        local pending_text = {}
        local pending_line = 0

        local function flush_pending()
            if #pending_text ~= 0 then
                chunks:append(table.concat(pending_text))
            end
            pending_text = {}
        end

        -- Find the next delimiter (opening if in text mode, closing otherwise).
        local function find_delimiter(line, from)
            local delimiter = (not special) and delimiters.open or delimiters.close
            return line:find(delimiter, from, plain_text)
        end

        -- Returns which kind of special mode we are in (raw, escape or code).
        --- @return Chunks.Kind, integer
        local function new_context(line, from)
            local delimiter_raw = delimiters.raw and #delimiters.raw or 0
            local delimiter_escape = delimiters.escape and #delimiters.escape or 0
            if delimiter_raw ~= 0 then
                if line:sub(from, from + delimiter_raw - 1) == delimiters.raw then
                    return elt.Chunks.RAW, from + delimiter_raw
                end
            end
            if delimiter_escape ~= 0 then
                if line:sub(from, from + delimiter_escape - 1) == delimiters.escape then
                    return elt.Chunks.ESCAPE, from + delimiter_escape
                end
            end

            return elt.Chunks.CODE, from
        end

        for line in elt.Parser.wrap_source(source) do
            line_count = line_count + 1
            -- Unread characters. Start at 0, to account for empty lines.
            local unread = 0

            -- Handle the "whole line is code" case (if its delimiter exists).
            if not special and delimiters.line and #delimiters.line ~= 0 then
                if line:sub(1, #delimiters.line) == delimiters.line then
                    flush_pending()
                    local rest = line:sub(#delimiters.line + 1)
                    chunks:append(rest, line_count, elt.Chunks.CODE)
                    -- We are done with the whole line, so we would `continue`,
                    -- if Lua had `continue`. This effectively skips the rest of
                    -- the outer `for` loop (by skipping the `while` below).
                    unread = #line + 1
                end
            end

            -- Loop over the line, splitting it by the delimiters.
            while unread <= #line do
                local from, to = find_delimiter(line, unread)

                -- If no `from`, then no delimiter was found and hence no change
                -- in state: we are still in normal or still in normal mode.
                if not from then
                    local rest = line:sub(unread)
                    -- Code should be added chunk by chunk because each new
                    -- template line produces a new *numbered* line in the
                    -- generated code. The others are accumulated till they end.
                    if special == elt.Chunks.CODE then
                        chunks:append(rest, line_count, special)
                    else
                        table.insert(pending_text, rest .. '\n')
                    end

                    break
                -- Now some delimiter found while being in text mode.
                elseif not special then
                    local normal_text = line:sub(unread, from - 1)
                    if #normal_text ~= 0 then
                        table.insert(pending_text, normal_text)
                    end
                    flush_pending()

                    -- Text handled till the delimiter. Next loop will handle
                    -- the special state from the new unread count.
                    special, unread = new_context(line, to + 1)
                    pending_line = line_count
                -- Last case: a delimiter was found while in special mode, so it
                -- is a closing one.
                else
                    local rest = line:sub(unread, from - 1)
                    if special == elt.Chunks.CODE then
                        chunks:append(rest, line_count, special)
                    else -- RAW or ESCAPE
                        table.insert(pending_text, rest)
                        chunks:append(table.concat(pending_text), pending_line, special)
                        pending_text = {}
                    end
                    unread = to + 1
                    special = false
                    -- Handle a delimiter at end of line. We consider this as
                    -- the newline being in text mode, and needing to be added.
                    if unread > #line then
                        table.insert(pending_text, '\n')
                    end
                end
            end -- while (loop over line contents)
        end -- for (loop over lines)
        flush_pending()

        return chunks
    end,
}
elt.Parser.__index = elt.Parser




--- Fixes the error message to be more useful to the template writer.
---
--- Tries to locate the line that the loader function failed to load, and if
--- found, changes it to a number line that resembles better to what the
--- template author wrote.
--- @param message string Text with the original error message.
--- @param code string Code that has failed to be loaded.
--- @return string # Amended error message.
elt.amend_error = function(message, code)
    -- Lua load errors look like: "[string "..."]:N: <description>"
    local number = message:match(':(%d+):')
    if not number then
        return message
    end
    -- Split the code back into an `string[]`, so we can iterate over it in
    -- reverse to find the closest `--[[N]]` at or before the reported line.
    local lines = {}
    for line in code:gmatch('[^\n]+') do
        table.insert(lines, line)
    end
    local pattern = '^%-%-%[%[(%d+)%]%]' -- Pattern to match `--[[N]]`.
    for line = tonumber(number), 1, -1 do
        local found = lines[line]:match(pattern)
        if found then
            return (message:gsub(':' .. number .. ':',
                                 ':~' .. found .. '(elt):'))
        end
    end
    return message
end




--- @param code_text string Text with the code to load.
--- @param code_name string|nil Name to pass to the `load` function.
elt.loader = function(code_text, code_name)
    -- Lua 5.2 sort of merged `loadstring` with `load`. LuaJIT supports both.
    local load_function = type(loadstring) == 'function' and loadstring or load
    local code_function, message = load_function(code_text, code_name)
    if not code_function then
        return nil, elt.amend_error(message --[[@as string]], code_text)
    end
    return code_function
end




elt.execute = function(f, environment, buffer, stringify, escape)
    environment = environment or {}
    buffer = buffer or {}
    local merged = setmetatable({}, {
        __index = function(_, name)
            local value = environment[name]
            if value ~= nil then
                return value
            else
                return _G[name]
            end
        end
    })
    setfenv(f, merged)
    return f(table.insert, buffer, stringify, escape)
end




elt.to_code = function(source, options)
    assert(type(options) == 'table' or type(options) == 'nil',
        'Options must be a table or nil')
    options = options or {}
    local parser = options.parser or elt.Parser:new()
    local delimiters = options.delimiters or elt.delimiters
    local chunks = parser:parse(source, delimiters)
    local generator = options.generator or elt.Generator:new()
    return generator:generate(chunks)
end




--- Compile a template into function that will format it when called.
---
--- The source for the template can be either:
--- * A string containing the lines of the template.
--- * An array table of strings containing the lines of the template.
--- * An iterator function to retrieve the lines one by one. An example of such
--- iterator is `io.lines`, which can be used to retrieve the text from a file
--- one by one.
---
--- The options are not required, but allow to override the defaults, which are:
--- * elt.Parser for parsing the template into fragments of code or text.
--- * elt.Generator for generating Lua code to be compiled.
--- * elt.delimiters for the characters to use as delimiters in templates.
--- * elt.code_name for the name to give the `load` function.
---
--- In case of success, a function is returned. The function returns a string of
--- text with the desired output when called:
---
--- ```lua
--- local template = elt.compile('<%= os.date("%Y-%m-%d %H:%M:%S") %>')
--- print(template()) --> "2026-03-17 12:05:40" (or whichever date you are on)
--- ```
---
--- The returned function has two optional parameters. The first one is the
--- environment that the template will see, where you will insert your own data
--- for the template to display. The second one is a "buffer" in the form of a
--- string array. If a buffer is passed, the function wont return a string, and
--- instead will add output to the buffer. See the documentation for details.
---
--- @param source string|table|function The template source to be compiled.
--- @param options? table|nil Extra options.
--- @return nil|function(environment?: table, buffer?: table): string?
--- @return nil|string
elt.compile = function(source, options)
    options = options or {}
    local implementation = elt.to_code(source, options)
    local code_name = options.code_name or 'elt'
    local escape = options.escape or elt.escape
    local stringify = tostring

    local template, message = elt.loader(implementation, code_name)

    if template then
        return function(environment, buffer)
            local output = elt.execute(template, environment, buffer, stringify, escape)
            if not buffer then
                return table.concat(output)
            end
        end
    else
        return nil, message
    end
end




elt.render = function(text, environment, options, buffer)
    local compiled, message = elt.compile(text, options)
    if compiled then
        return compiled(environment, buffer)
    else
        return nil, message
    end
end

return elt

